#include "libft.h"

size_t	ft_wordlen(char const *str)
{
	size_t i;

	if (str == NULL)
		return (0);
	i = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	while (str[i] && !((str[i] >= 9 && str[i] <= 13) || str[i] == ' '))
		i++;
	return (i);
}
